n1 = int(input("Valor 1:::"))
n2 = int(input("Valor 2:::"))

print ("A soma  entre {} e {} é {}".format(n1,n2,(n1+n2)))
print ("A subtração entre {} e {} é {}".format(n1,n2,(n1-n2)))
print ("A multiplicação entre {} e {} é {}".format(n1,n2,(n1*n2)))
print ("A divisão  entre {} e {} é {:.3f}".format(n1,n2,(n1/n2)), end =">>>")
print ("Resto da divisão entre {} e {} é {:}".format(n1,n2,(n1%n2)), end =">>>")     #resto da divisão
print ("A divisão inteira entre {} e {} é {}".format(n1,n2,(n1//n2)))                #divisão inteira
print ("Potência entre {} e {} é {}".format(n1,n2,(n1**n2)))                         #potência

